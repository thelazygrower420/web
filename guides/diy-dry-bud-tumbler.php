		<h2>Step 2: Prepare the Lids & Axle</h2>
		<div class="content-image-wrapper align-right">
			<div class="gallery-item"><a href="/images/tumbler/tumbler-lids-1.jpg" data-caption="Lids with center holes drilled for the PVC axle."><img src="/images/tumbler/th-tumbler-lids-1.jpg" alt="Drilled bucket lids"></a></div>
        </div>
        <p>Now we'll prepare the end caps and the PVC axle that will hold everything together and allow it to spin.</p>
        <ol>
            <li><strong>Drill the Lids:</strong> Using the step drill, carefully drill a hole in the center of each bucket lid that the ¾" PVC pipe can pass through snugly, but not so tight that it binds.</li>
            <li><strong>Prep the PVC Axle:</strong> Place the finished lids on either side of the cylinder. Feed the PVC tube through the center holes, leaving one end longer for the turning crank.</li>
            <li><strong>Mark & Drill Dowel Holes:</strong> While holding the assembly tightly, mark where you want the dowel pins to go through the PVC pipe to lock the lids in place. Drill these holes through both sides of the PVC pipe on both ends.</li>
            <li><strong>Create the Crank:</strong> On the longer end of the PVC pipe, drill a hole through both sides for your crank handle (the stainless steel straw or a small dowel).</li>
        </ol>
