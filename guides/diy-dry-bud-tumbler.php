        <h2>Planning & Materials</h2>
        
        <h3>Supplies You'll Need:</h3>
        <ul>
            <li><strong>Hardware Cloth:</strong> ¼" or ½" mesh (or both for different grades).</li>
            <li><strong>Bucket Lids (x2):</strong> These determine the diameter of your drum.</li>
            <li><strong>Plastic Tub:</strong> Must be deeper and slightly wider than your drum.</li>
            <li><strong>PVC Pipe:</strong> ¾" diameter for the main axle.</li>
            <li><strong>PVC Coupler:</strong> ¾" diameter.</li>
            <li><strong>Dowel Rod:</strong> ⅝" diameter, for locking pins.</li>
            <li><strong>26 Gauge Wire:</strong> For sewing the cylinder seam.</li>
            <li><strong>Rubber O-Ring:</strong> To fit snugly on the ¾" PVC pipe.</li>
            <li><strong>Stainless Steel Straw:</strong> Or a small dowel for the turning crank.</li>
            <li><strong>Bungee Cord:</strong> To secure the access door.</li>
            <li><strong>Optional:</strong> Metal clasps or hinges for a more permanent door.</li>
        </ul>

        <h3>Tools You'll Need:</h3>
        <ul>
            <li>Wire Snips</li>
            <li>Drill</li>
            <li>Step Drill Bit</li>
            <li>½" or ⅝" Drill Bit</li>
            <li>Razor Blade or Deburring Tool</li>
            <li>Hacksaw</li>
        </ul>

        <h2>Step 1: Create the Cylinder</h2>
        <p>The first step is to form the main sifting drum using the hardware cloth and bucket lids.</p>
        <ol>
            <li><strong>Determine Length:</strong> The groove on the bucket lid is where the edge of your hardware cloth will sit. Mock up the cloth around the lid to find the circumference, adding a 3-4 square overlap. Cut the cloth to this length.</li>
            <li><strong>Form the Seam:</strong> Overlap the edges of the cloth and use small pieces of the 26-gauge wire to temporarily tack the seam together in a few spots.</li>
            <li><strong>Test Fit:</strong> Fit the mesh cylinder onto both bucket lids to ensure a proper, snug fit. Adjust if necessary.</li>
            <li><strong>Sew the Seam:</strong> Once the fit is good, "sew" the seam by threading a long piece of the 26-gauge wire through the overlapping squares. Start and end inside the lid groove to hide the wire ends.</li>
            <li><strong>Cut the Door:</strong> Cut an access door/hole large enough to easily add and remove material. Then, cut a slightly larger "flap" from the leftover hardware cloth to serve as the door cover.</li>
        </ol>
