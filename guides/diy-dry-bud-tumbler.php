        <h2>Step 3: Prepare the Tub & Final Assembly</h2>
        <p>The final step is to create the support structure in the main plastic tub.</p>
        <ol>
            <li><strong>Mark the Holes:</strong> Place the PVC tube on the tub to mark where to drill the axle holes. Aim for the drum to sit about halfway deep inside the tub.</li>
            <li><strong>Drill & Cut:</strong> Drill a round hole on one side of the tub. On the other side, drill a hole and then use a hacksaw to cut down from the top edge, creating a U-shaped notch. This allows you to easily lift the drum in and out.</li>
            <li><strong>Assemble:</strong> Place one end of the axle through the round hole and rest the other end in the U-shaped notch.</li>
            <li><strong>Secure:</strong> Slide the PVC coupler onto the axle on the round-hole side, and the O-ring onto the axle on the notch side. These will prevent the drum from sliding side-to-side while spinning.</li>
            <li><strong>Attach the Crank:</strong> Insert your crank handle, and you're ready to tumble! Use the bungee cord to secure the door flap during operation.</li>
        </ol>
