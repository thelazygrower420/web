        <h2>Choosing Your Carrier: Notes on Different Fats & Solvents</h2>
        <h3>Fat-Based Carriers (for Edibles & Topicals)</h3>
        <ul>
            <li><strong>Coconut Oil / MCT Oil:</strong> The gold standard. Its high saturated fat content makes it an excellent carrier for cannabinoids. Refined coconut oil has a neutral taste, while unrefined has a stronger coconut flavor. MCT oil remains liquid at room temperature, making it great for dropper bottles.</li>
            <li><strong>Butter (Clarified/Ghee):</strong> Perfect for baking. It's crucial to use <strong>clarified butter (ghee)</strong>. Regular butter contains water and milk solids which do not bind with THC and can cause your infusion to go rancid faster. To clarify, simply melt butter on low and skim off the white milk solids that rise to the top.</li>
            <li><strong>Cocoa Butter:</strong> The best choice for making homemade cannabis chocolates or suppositories. It's solid at room temperature and has a rich flavor that pairs well with cannabis.</li>
        </ul>

        <h3>Alcohol & Glycerin Carriers (for Tinctures)</h3>
        <ul>
            <li><strong>High-Proof Alcohol (Everclear, Spirytus):</strong> For potent tinctures. Use food-grade, high-proof (190 proof / 95% ABV) alcohol. The process is much faster (a "quick wash" or QWET method can take minutes, not hours) but requires extreme caution. <strong>NEVER use an open flame or gas stove when working with high-proof alcohol.</strong></li>
            <li><strong>Vegetable Glycerin (VG):</strong> A non-alcoholic, sweet-tasting alternative. VG is not as efficient at extraction as alcohol, resulting in a less potent tincture. Infusion time is similar to fats (several hours) and it's great for those avoiding alcohol.</li>
        </ul>

