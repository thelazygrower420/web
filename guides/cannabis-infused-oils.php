        <p>Once you've mastered <a href="/guides/decarb-cannabis.php">decarboxylation</a>, the next step is infusion. This process extracts the activated cannabinoids (like THC) from the plant material into a carrier substance. This guide will walk you through making versatile infusions using fats like oil and butter, as well as potent tinctures using alcohol.</p>

        <h2>The Science: How Infusion Works</h2>
        <p>Understanding why infusion works helps you make better products. It comes down to a simple principle: solubility.</p>
        
        <h3>THC and Fats (Lipophilicity)</h3>
        <p>Cannabinoids like THC are <strong>lipophilic</strong>, which is a scientific term for "fat-loving." Their molecular structure is non-polar, causing them to be naturally repelled by water (which is polar) but strongly attracted to fats and oils (which are also non-polar). When you gently heat cannabis in a fat, the activated THC molecules happily leave the plant material and dissolve into the surrounding fat molecules, creating a potent and stable infusion.<br><em>Source: Based on the chemical principle "like dissolves like." Non-polar cannabinoids dissolve in non-polar lipids (fats).</em></p>
        
        <h3>THC and Alcohol (Solvent Extraction)</h3>
        <p>High-proof alcohol acts as a powerful <strong>solvent</strong>. Instead of just attracting the THC, it actively strips the trichomes—the tiny, crystal-like glands on the cannabis flower that contain the vast majority of cannabinoids and terpenes—directly from the plant material. This is a very efficient and fast method, resulting in a potent liquid extract often used for tinctures or to create concentrates like FECO/RSO.</p>

