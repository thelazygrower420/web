        <h3>Instructions:</h3>
        <ol>
            <li><strong>Combine and Melt:</strong> Gently melt your chosen fat (oil/butter) in your double boiler or pot. Do not let it smoke.</li>
            <li><strong>Add Cannabis:</strong> Once melted, add your ground, decarbed cannabis. Stir well to combine.</li>
            <li><strong>Infuse Low and Slow:</strong> Keep the mixture on very low heat for 2-4 hours. The liquid should never boil or simmer aggressively. Stir every 30-45 minutes.</li>
            <li><strong>Strain Carefully:</strong> Set your cheesecloth or strainer over your storage container. Slowly pour the hot mixture through it.</li>
            <li><strong>Squeeze (Optional but Recommended):</strong> Once cool enough to handle, gather the cheesecloth and gently squeeze out all the remaining oil from the plant material. This is where much of the potency is!</li>
            <li><strong>Cool and Store:</strong> Let the infused product cool to room temperature, then seal and store in a cool, dark place. Refrigeration will extend its shelf life.</li>
        </ol>

