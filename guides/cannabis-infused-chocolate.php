        <h3>Ingredients:</h3>
        <ul>
            <li><strong>1/2 cup (120 ml)</strong> Cannabis-Infused Coconut Oil or food-grade Cocoa Butter.</li>
            <li><strong>1/2 cup (50g)</strong> Unsweetened Cocoa Powder (Dutch-processed works well).</li>
            <li><strong>1/2 cup (50g)</strong> Powdered Sugar (can substitute with honey or agave, but may affect texture).</li>
        </ul>
