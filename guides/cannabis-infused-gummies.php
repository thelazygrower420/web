        <h2>Step-by-Step Instructions</h2>
        <ol>
            <li><strong>Bloom the Gelatin:</strong> In your small container, pour the fruit juice. Evenly sprinkle all gelatin packets (both unflavored and the Starburst Jello) over the liquid. Let it sit for 5-10 minutes to "bloom" and absorb the liquid.</li>
            <li><strong>Combine Oil and Lecithin:</strong> In the small saucepan, combine your infused oil and the soy lecithin. Whisk thoroughly until well blended. This is a key step for emulsification.</li>
            <li><strong>Heat and Combine:</strong> Add the bloomed gelatin mixture to the saucepan with the oil. Turn the stove to low heat and stir continuously with your whisk. Don't worry if it seems clumpy at first; it will smooth out as the gelatin melts.</li>
            <li><strong>Add Remaining Ingredients:</strong> Once the mixture is smooth, add the potassium sorbate, citric acid, and corn syrup. Stir constantly after each addition. (A light spray of cooking spray on your measuring spoon will prevent the corn syrup from sticking.) If using, add your optional candy flavoring now.</li>
            <li><strong>Mold the Gummies:</strong> The mixture will be thick. Carefully transfer it into your squeeze bottle. Working quickly, fill your gummy molds.</li>
            <li><strong>Dry the Gummies:</strong> Let the molds sit at room temperature for about 30 minutes to set. Then, pop the gummies out of the molds and place them on a drying rack. Allow them to air-dry for 24-48 hours. This step is crucial for achieving that perfect, chewy, store-bought texture and preventing them from getting sticky.</li>
        </ol>

        <h2>Storage & Tips</h2>
        <ul>
            <li><strong>Storage:</strong> Once fully dried, these gummies are shelf-stable and do not need refrigeration. Store them in an airtight container for several months.</li>
            <li><strong>Lecithin:</strong> If using lecithin granules instead of powder/liquid, you may want to gently warm the juice and oil and blend with an immersion blender before adding the gelatin to ensure it fully dissolves.</li>
            <li><strong>Don't Skip the Drying Step!</strong> Seriously, this is the most important part for texture and longevity. Be patient!</li>
        </ul>

        <h3>Bonus Tip: Sour Coating</h3>
        <p>For an extra-special sour touch, mix granulated sugar and citric acid (a 2:1 or 3:1 ratio works well). After your gummies have dried, you can lightly mist them with water and then toss them in this mixture until evenly coated. Let them dry again for a few hours.</p>

