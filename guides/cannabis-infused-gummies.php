        <h2>What You'll Need</h2>

        <h3>Ingredients:</h3>
        <ul>
            <li>1 box Starburst Jello (large 6oz size recommended).</li>
            <li>4 packets unflavored gelatin (e.g., Knox).</li>
            <li>½ cup fruit juice (apple or white grape work well).</li>
            <li>½ cup <a href="/guides/cannabis-infused-oils.php">cannabis-infused</a> MCT or Coconut Oil.</li>
            <li>2 teaspoons soy lecithin (powder or liquid recommended).</li>
            <li>1 tablespoon light corn syrup.</li>
            <li>1 teaspoon citric acid (for flavor).</li>
            <li>¼ teaspoon potassium sorbate (as a preservative).</li>
            <li>1-2 drops candy flavoring (optional).</li>
            <li>Cooking spray (e.g., Pam).</li>
        </ul>

        <h3>Equipment:</h3>
        <ul>
            <li>Small container or bowl (for blooming gelatin).</li>
            <li>Small saucepan.</li>
            <li>Whisk (silicone-coated preferred).</li>
            <li>Squeeze bottle (for easy molding).</li>
            <li>Silicone gummy molds.</li>
            <li>Drying rack.</li>
        </ul>

