        <h3>2. Sous Vide Method</h3>
        <ul>
            <li><strong>Equipment:</strong> Sous vide machine, vacuum sealer bags, water bath.</li>
            <li><strong>Process:</strong>
                <ol>
                    <li>Preheat your sous vide water bath to 203°F (95°C).</li>
                    <li>Place your cannabis in a vacuum sealer bag and seal it tightly.</li>
                    <li>Submerge the bag in the water bath and cook for 90 minutes.</li>
                    <li>Remove the bag.</li>
                </ol>
            </li>
        </ul>

        <h2>Tips for Successful Decarboxylation</h2>
        <ul>
            <li><strong>Grind Consistency:</strong> Finely grind your cannabis for even heating and decarboxylation.</li>
            <li><strong>Temperature Control:</strong> Use an oven thermometer, or two to ensure accurate temperature. Overheating can degrade THC.</li>
            <li><strong>Time:</strong> Don't rush the process. Proper decarboxylation takes time.</li>
            <li><strong>Smell:</strong> Your cannabis will have a toasted, nutty aroma when it's properly decarboxylated.</li>
            <li><strong>Important Note:</strong> Decarboxylation produces a strong odor. Ensure proper ventilation in your kitchen.</li>
        </ul>
