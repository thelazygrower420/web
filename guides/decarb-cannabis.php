        <h2>What is Decarboxylation?</h2>
        <p>Cannabis in its raw form contains <strong>THCA</strong> (tetrahydrocannabinolic acid), a non-intoxicating compound. When you heat cannabis, THCA converts to <strong>THC</strong> (tetrahydrocannabinol), the psychoactive compound that gets you high. This process of using heat to activate cannabis is called decarboxylation.</p>

        <h2>Why Decarboxylate?</h2>
        <p>If you're using cannabis for edibles or other infusions, decarboxylation is essential. Without it, you won't experience the full effects of the THC, and your edibles might be underwhelming.</p>

        <h2>Methods for Decarboxylation</h2>
        <p>Here are the most common methods for decarboxylating cannabis:</p>
		
        <h3>1. Oven Method</h3>

        <div class="content-image-wrapper align-left">
                <a href="/images/decarb/decarb-oven-temp.jpg" data-caption="Set oven to 240°F (115°C)">
