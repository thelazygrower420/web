        <ul>
            <li><strong>Equipment:</strong> Baking sheet, parchment paper, oven.</li>
            <li><strong>Process:</strong>
                <ol>
                    <li>Preheat your oven to 240°F (115°C).</li>
                    <li>Finely grind your cannabis.</li>
                    <li>Spread the ground cannabis evenly on a baking sheet lined with parchment paper.</li>
                    <li>Bake for 30-40 minutes, stirring occasionally.</li>
                    <li>The cannabis should turn golden brown when it's done.</li>
                </ol>
            </li>
        </ul>
