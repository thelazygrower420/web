        <h2 id="indoor">The Indoor Environment: A Tech-Driven Secret Garden</h2>
        <p>I've always been fascinated with cultivating my own cannabis, but I wanted to do it in a way that reflected my passion for technology and automation. This led me to create a 7x9 ft indoor grow room that's less about traditional gardening and more about code, control systems, and data.</p>
		
        <h3>The Setup: Behind the Murphy Door</h3>
        <p>True to the 'secret garden' name, the entrance is completely hidden behind a custom-built Murphy door. Once inside, you're greeted not by the earthy aroma of soil, but by the hum of fans and the glow of LED lights. This space is the heart of my automated growing experiments.</p>
        
        <p>The Murphy door, a clever disguise, ensures privacy and blends seamlessly with the surrounding environment. It's a testament to the integrated design, where functionality meets discretion. The interior is optimized for growth, with careful consideration given to every detail from air circulation to light penetration.</p>
        <p>This room is where I test various automated systems, from irrigation schedules to environmental controls. It's a continuous learning process, with each cycle providing valuable data for refinement and improvement.</p>
