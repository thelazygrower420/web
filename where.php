        <h3>Infrastructure: Power & Plumbing</h3>
        <p>To ensure a stable and reliable environment, the grow room is equipped with its own electrical sub-panel. Dedicated cold and hot water lines, along with a proper drain, provide the necessary infrastructure for irrigation and maintenance, completely separating it from household utilities.</p>
        
        <p>The dedicated electrical panel ensures that the grow room operates independently and safely, preventing any interference with the main household power supply. The separate plumbing system guarantees a clean and consistent water source for irrigation, minimizing the risk of contamination and simplifying maintenance.</p>
        <p>This robust infrastructure is key to the long-term success and stability of the automated indoor grow environment. It provides a foundation for future expansions and technological upgrades.</p>
